function cmToMeter(cm){
var m = cm * 0.01
return `${cm} cm is ${m} meter`
}

console.log(cmToMeter(100))

function cmTomm(mm){
    var cm = mm * 10
    return `${cm} cm is ${mm} milimeter`
    }
    
    console.log(cmTomm(100))

